<p>หาดกะรน มีเนินทรายเป็นแนวยาว ปกคลุมด้วยเถาผักบุ้งทะเลเขียวสดดูละลานตาจนเป็นเอกลักษณ์ของหาดแห่งนี้ ทางเทศบาลได้จัดทำสะพานไม้เดินลงหาดเป็นระยะๆ เพื่อไม่ให้นักท่องเที่ยวเหยียบย่ำผักบุ้งทะเล เมื่อไปเที่ยวหาดกะรน จะเห็นผักบุ้งทะเลอวดดอกสีม่วงอ่อน บานต้อนรับนักท่องเที่ยวอยู่ตลอดปี</p>
