<h2>3.ชิโนโปรตุกีส ย่านเมืองภูเก็ต<i>(Sino Portuguese)</i></h2></a>
<p style="text-align:center" >
<img src="https://static.naewna.com/uploads/news/source/388271.jpg" alt="Sino Portuguese" width="500" height="300">
</p>
<p>ชิโนโปรตุกีส (Sino Portuguese) ตึกเก่าที่ตั้งอยู่ในย่านการค้าเก่าเเก่ของเมือง เป็นอาคารสไตล์"ชิโนโปรตุกีส"ที่ผสมผสานเอาความเป็นศิลปะตะวันตกและตะวันออก เข้าไว้ด้วยกันอย่างกลมกลืน จนเป็นเอกลักษณ์ที่โดดเด่นของเมืองภูเก็ต ตึกเก่าเหล่านี้กระจายอยู่ทั่วตัวเมืองภูเก็ต สามารถเดินชมได้อย่างต่อเนื่องนอกจากนี้ยังมีร้านอาหารอร่อยหลายเจ้าให้เที่ยวไปกินไปอย่างเพลิดเพลิน ชิโนโปรตุกีสจัดให้เป็นอีกหนึ่งตัวเลือกของการท่องเที่ยว จัดให้มีเส้นทางเดินชมเมืองเก่าภูเก็ต เพื่อให้นักท่องเที่ยวได้ สัมผัสกับความสวยงามของบ้านเรือนเก่าแก่ของภูเก็ต และสถาปัตยกรรม ชิโนโปรตุกีส ที่สวยงาม และที่ห้ามพลาดคือไปถ่ายรูปกับหอนาฬิกาที่ตั้งตระหง่านสวยงามนั่นเอง โดยย่านเมืองเก่านี้จะสวยงามทั้งกลางวันและกลางคืนเลย หากใครอยากจะมาลองเดินชมใกล้ๆ เวลากลางคืนก็สามารถมาในวันอาทิตย์ที่มีหลาดใหญ่ (ถนนคนเดิน) ก็ได้เช่นกัน พร้อมๆกับได้สัมผัสวัฒนธรรมและวิถีชีวิตความเป็นอยู่ของคนภูเก็ต</p>
