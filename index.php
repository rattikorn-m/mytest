<h2>2.พิพิธภัณฑ์ภาพลวงตา ภูเก็ตทริกอายมิวเซียม<p><i>(Phuket Trickeye Museum)</i></p></h2></a>
<p style="text-align:center" >
<img src="http://www.adaytochill.com/wp-content/uploads/2014/05/Trickeye13.jpg" alt="Phuket Trickeye Museum" width="500" height="300">
</p>
<p>ภูเก็ต ทริกอาย มิวเซียม (Phuket Trickeye Museum)พิพิธภัณฑ์ศิลปะ ที่รวบรวมเอาภาพวาด 3มิติ มาไว้มากมาย สามารถให้ทุกท่านได้สัมผัสกับภาพจิตรกรรมเสมือนจริง สร้างสรรค์ภาพโดยจิตรกรชาวเกาหลี ใช้ระยะเวลาในการวาดร่วม 3 เดือน เพื่อร่วมกันเนรมิตพิพิธภัณฑ์แห่งนี้ให้เป็นจิตรกรรมบนฝาผนังเสมือนจริง ที่ต้องใช้เทคนิคการวาดภาพให้สามารถลวงตาผู้ชมได้ ทำให้ผู้ชมสัมผัสและมีส่วนร่วมกับผลงานศิลปะได้อย่างใกล้ชิด ให้ความรู้สึกเสมือนจริง ประหนึ่งว่าเป็นส่วนหนึ่งของภาพจิตรกรรม</p>
<h4>ค่าเข้า : ฟรีสำหรับเด็กสูงไม่ถึง 100 ซม.เด็ก(อายุ4-10ปี) 150 บาท ผู้ใหญ่ 250 บาท</h4>
