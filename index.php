<h2>5.พระพุทธมิ่งมลคลเอกนาคคีรี<p><i>(Phraphutthamingmongkhol akenagakhiri)</i></p></h2></a>
<p style="text-align:center" >
<img src="https://encrypted-tbn0.gstatic.com/images?q=tbn%3AANd9GcTYZp4RBMOb9MJVTsz5L6DhEFEaSJYLZ5FcWvxr1LEZ4IIdxfmb"alt="Phraphutthamingmongkhol akenagakhiri" width="500" height="300">
</p>
<p> พระพุทธมิ่งมลคลเอกนาคคีรี มาภูเก็ตก็ต้องไปไหว้พระพุทธมิ่งมงคลเอกนาคคีรีหรือพระใหญ่แห่งเมืองภูเก็ตก่อนเลย พระใหญ่ประดิษฐานอยู่บนยอดเขานาคเกิด เป็นพระพุทธรูปปางมารวิชัยขนาดใหญ่ สามารถสังเกตุเห็นได้จากหลายจุดในภูเก็ต ผิวของพระพุทธรูปนั้นประดับด้วยหินอ่อนหยกขาวจากพม่า เป็นงานประณีตที่สวยงาม นอกจากจะมาไหว้พระแล้วยังสามารถทำพิธีสะเดาะเคราะห์ได้ด้วยการแขวนกระดิ่งทองเหลืองได้อีกด้วย
</p>
